soma = 1 + 1
print("1 + 1 = ", soma)

subtracao = 1 - 1
print("1 - 1 = ", subtracao)

multiplicacao = 2 * 2
print("2 x 2 = ", multiplicacao)

divisao = 5 / 2
print("5 / 2 = ", divisao)
print("Tipo da variável do resultado da divisão = ", type(divisao))

print("Valor em inteiro = ", int(divisao))

print("Valor em float = ", float(divisao))

modulo = 5 % 2
print("Modulo = ", modulo)

divisao = 5 // 2
print("5 // 2 = ", divisao)
print("Tipo da variável do resultado da divisão = ", type(divisao))
